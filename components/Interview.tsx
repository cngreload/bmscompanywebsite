"use client";
import { TracingBeam } from "@/components/ui/tracing-beam";
import Image from "next/image";
import { cn } from "@/lib/utils";
import React, { useState, useEffect } from "react";

interface TracingbeamProps
{
    className?: string;
}

export function Tracingbeam ( { className }: TracingbeamProps )
{
    const [ isScrolled, setIsScrolled ] = useState( false );

    useEffect( () =>
    {
        const handleScroll = () =>
        {
            if ( window.scrollY > 100 )
            {
                setIsScrolled( true );
            } else
            {
                setIsScrolled( false );
            }
        };

        window.addEventListener( 'scroll', handleScroll );

        // Cleanup the event listener on component unmount
        return () =>
        {
            window.removeEventListener( 'scroll', handleScroll );
        };
    }, [] );

    return (
        <TracingBeam className={ cn( "px-6", className, isScrolled ? "bg-black text-white" : "bg-white text-black" ) }>
            <div className="max-w-2xl mx-auto antialiased pt-4 relative">
                { Content.map( ( item, index ) => (
                    <div key={ `content-${ index }` } className="mb-10">
                        <h2
                            className={ cn(
                                "rounded-full text-sm w-fit px-4 py-1 mb-4",
                                isScrolled ? "bg-emerald-500 text-white" : "bg-gray-100 text-emerald-600"
                            ) }
                        >
                            { item.badge }
                        </h2>
                        <p className="text-xl mb-4">
                            { item.title }
                        </p>
                        <div className="text-sm prose prose-sm dark:prose-invert">
                            { item?.image && (
                                <Image
                                    src={ item.image }
                                    alt="blog thumbnail"
                                    height={ 1000 }
                                    width={ 1000 }
                                    className="rounded-lg mb-10 object-cover w-full max-w-full"
                                />
                            ) }
                            { item.description }
                        </div>
                    </div>
                ) ) }
            </div>
        </TracingBeam>
    );
}

